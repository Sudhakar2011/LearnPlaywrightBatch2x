let age = 10; // number literal

let isStudnet = true; // boolean literal
let pi = 3.14; // number literal
let name = "Sudhakar"; // string literal
let nullValue = null; // null literal
let undefinedValue = undefined; // undefined literal

// typeof operator to check the type of literals  // it will shows data type of the literal

console.log(typeof age); // "number"
console.log(typeof isStudnet); // "boolean"
console.log(typeof pi); // "number"
console.log(typeof name); // "string"
console.log(typeof nullValue); // "object"
console.log(typeof undefinedValue); // "undefined"