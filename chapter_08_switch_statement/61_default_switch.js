

let day = 10;

switch (day) {

    case 1:
        console.log("Mon");
        break;
    case 2:
        console.log("Tue");
        break;
    case 3:
        console.log("Wed");
        break;
    case 4:
        console.log("Thu");
        break;
    default:
        console.log("Invalid day");
}